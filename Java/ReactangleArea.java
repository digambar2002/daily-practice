public class ReactangleArea {

    public static void main(String[] args) {
        int length = 5;
        int breadth = 7;

        // Formula to calculate area of reactangle
        int area = length * breadth;

        // Formula to Perimeter area of reactangle
        int perimeter = (2 * length) + (2 * breadth);

        System.out.println("Area of reactangle is: " + area);
        System.out.println("Perimeter of reactangle is: " + perimeter);
    }
}